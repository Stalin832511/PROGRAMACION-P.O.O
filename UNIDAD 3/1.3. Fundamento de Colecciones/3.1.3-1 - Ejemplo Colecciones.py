# Ejemplo de uso de colecciones en Python

# Lista
lista_ejemplo = [1, 2, 3, 4, 5]
print("Lista:", lista_ejemplo)

# Diccionario
diccionario_ejemplo = {"nombre": "Juan", "edad": 25, "ciudad": "Quito"}
print("Diccionario:", diccionario_ejemplo)

# Conjunto
conjunto_ejemplo = {1, 2, 3, 4, 5}
print("Conjunto:", conjunto_ejemplo)

# Tupla
tupla_ejemplo = (10, 20, 30, 40, 50)
print("Tupla:", tupla_ejemplo)
